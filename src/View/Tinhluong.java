    private void ShowTable() {
        model = (DefaultTableModel) Tableluong.getModel();
        model.setRowCount(0);
        list = new TinhLuongDB().listLuong();
        for (Luong l : list) {
            model.addRow(new Object[]{l.getID(), l.getMaNV(), l.getLuongChinhThuc(), l.getThang(), l.getNam()});
        }
    }
