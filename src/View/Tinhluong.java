    private void TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TimKiemActionPerformed
        // TODO add your handling code here:      
        int a = jMonthChooser1.getMonth() + 1;
        int b = jYearChooser1.getYear();
        String name = ComboTen.getSelectedItem().toString();
        model = (DefaultTableModel) Tableluong.getModel();
        TinhLuongDB nvd = new TinhLuongDB();
        list = nvd.FindByma(name, a, b);
        System.out.println(list.getFirst().getNam());
        model.setRowCount(0);
        for (Luong l : list) {
            if (l.getLuongChinhThuc() == 0) {               
                Tinhbtn.setVisible(true);
                return;
            }
            model.addRow(new Object[]{l.getID(), l.getMaNV(), l.getLuongChinhThuc(), l.getThang(), l.getNam()});
        }

    }//GEN-LAST:event_TimKiemActionPerformed

