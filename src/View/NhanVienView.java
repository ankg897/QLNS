    private void TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TimKiemActionPerformed
        // TODO add your handling code here:
        String a = timkiem.getText();
        model = (DefaultTableModel) ResultTable.getModel();
        if (a.length() > 0) {
            NhanVienDB nvd = new NhanVienDB();
            list = nvd.FindByName(a);
            model.setRowCount(0);
            for (NhanVien nv : list) {
                model.addRow(new Object[]{nv.getMaNv(), nv.getHoten(), nv.getVitri(), nv.getBophan(), nv.getNgayThangNamSinh(),
                    nv.getGioitinh(), nv.getEmail(), nv.getSdt(), nv.getMaPB(), nv.isIsActive()});
            }
        }
    }//GEN-LAST:event_TimKiemActionPerformed

    private void SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SuaActionPerformed
        // TODO add your handling code here:
        try {
            index = ResultTable.getSelectedRow();
            NhanVien nv = list.get(index); // lấy dòng ra
            new NhanVienDB().Delete(nv.getMaNv());
            if (CheckMA() && CheckMail() && CheckSDT() && CheckTen()) {
                nv.setMaNv(MaNv.getText());
                nv.setHoten(Hoten.getText());
                nv.setVitri(viTribox.getSelectedItem().toString());
                nv.setBophan(combophan.getSelectedItem().toString());
                nv.setNgayThangNamSinh(ngayThangNamSinh.getText());
                if (comgioitinh.getSelectedItem().toString() == "Nam") {
                    nv.setGioitinh("1");
                } else {
                    nv.setGioitinh("0");
                }
                nv.setEmail(email.getText());
                nv.setSdt(SDT.getText());
                nv.setMaPB(MaPhongBan.getText());
             //   nv.setIsActive(true);
                NhanVienDB nvd = new NhanVienDB();
                nvd.AddNhanVien(nv);
                ShowTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }
    }//GEN-LAST:event_SuaActionPerformed

