    private boolean CheckMA() {
        String a = MaNv.getText();
        String b = MaPhongBan.getText();
        if (a.isEmpty() || b.isBlank()) {
            return false;
        }
        for (int i = 2; i < a.length(); i++) {
            if (a.charAt(0) != 'N' && a.charAt(1) != 'V' && !Character.isDigit(a.charAt(i))) {
                return false;
            }
        }
        for (int i = 2; i < a.length(); i++) {
            if (b.charAt(0) != 'P' && b.charAt(1) != 'B' && !Character.isDigit(b.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    private boolean CheckMail() {
        String a = email.getText();
        if (a.isEmpty()) {
            return false;
        }
        for (int i = 0; i < a.length(); i++) {
            if (!a.contains("@") || !a.contains(".")) {
                return false;
            }
        }
        return true;
    }

    private boolean CheckTen() {
        String a = Hoten.getText();
        if (a.isEmpty()) {
            return false;
        }
        for (int i = 0; i < a.length(); i++) {
            if (Character.isDigit(a.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    private boolean CheckSDT() {
        String a = SDT.getText();
        if (a.isEmpty()) {
            return false;
        }
        for (int i = 0; i < a.length(); i++) {
            if (Character.isLetter(a.charAt(i))) {
                return false;
            }
        }
        return true;
    }
