    private void TableModelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableModelMouseClicked
        // TODO add your handling code here:
        index = TableModel.getSelectedRow();
        HopDong hd = list.get(index);
        ID.setText(hd.getID());
        ngaybatdau.setText(hd.getNgayBatDau());
        ngayketthuc.setText(hd.getNgayKetThuc());
        langiahan.setSelectedItem(hd.isGiaHan());
        lanky.setText(hd.getSolanky() + "");
        manv.setText(hd.getMaNV());
        Ten.setSelectedItem(hd.getTen());
    }//GEN-LAST:event_TableModelMouseClicked

