    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        // TODO add your handling code here:
        if (Check()) {
            HopDong hd = new HopDong();
            hd.setID(ID.getText());
            hd.setTen(Ten.getSelectedItem().toString());
            hd.setNgayBatDau(ngaybatdau.getText());
            hd.setNgayKetThuc(ngayketthuc.getText());
            if (langiahan.getSelectedIndex() == 0) {
                hd.setGiaHan(true);
            } else {
                hd.setGiaHan(false);
            }
            hd.setSolanky(Integer.parseInt(lanky.getText()));
            hd.setMaNV(manv.getText());
            HopDongDB dbl = new HopDongDB();
            dbl.addHopDong(hd);
            ShowTable();
        } else {
            JOptionPane.showMessageDialog(null, "Thông tin không hợp lệ");
        }
    }//GEN-LAST:event_themActionPerformed

