    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        // TODO add your handling code here:
        index = TableModel.getSelectedRow();
        HopDong hd = list.get(index);
        int ch = JOptionPane.showConfirmDialog(null, "Bạn muốn xóa hợp đồng này", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (ch == JOptionPane.YES_OPTION) {            
            HopDongDB dbl = new HopDongDB();
            dbl.DeleteHD(hd.getMaNV());
            ShowTable();
        }
        ShowTable();
    }//GEN-LAST:event_xoaActionPerformed

    private void TimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TimkiemActionPerformed
        // TODO add your handling code here:
        String a = no.getText();
        model = (DefaultTableModel) TableModel.getModel();
        if (a.length() > 0) {
            list = new HopDongDB().FindByName(a);
            model.setRowCount(0);
            for (HopDong hd : list) {
                model.addRow(new Object[]{hd.getID(), hd.getTen(), hd.getNgayBatDau(), hd.getNgayKetThuc(),
                    hd.isGiaHan(), hd.getSolanky(), hd.getMaNV()});
            }
        }
    }//GEN-LAST:event_TimkiemActionPerformed

    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed
        // TODO add your handling code here:
        try {
            index = TableModel.getSelectedRow();
            HopDong hd = list.get(index);
            new HopDongDB().DeleteHD(hd.getID());
            if (Check()) {
                hd.setID(ID.getText());
                hd.setTen(Ten.getSelectedItem().toString());
                hd.setNgayBatDau(ngaybatdau.getText());
                hd.setNgayKetThuc(ngayketthuc.getText());
                if (langiahan.getSelectedIndex() == 0) {
                    hd.setGiaHan(true);
                } else {
                    hd.setGiaHan(false);
                }
                hd.setSolanky(Integer.parseInt(lanky.getText()));
                hd.setMaNV(manv.getText());
                HopDongDB dbl = new HopDongDB();
                dbl.addHopDong(hd);
                ShowTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }
    }//GEN-LAST:event_suaActionPerformed

